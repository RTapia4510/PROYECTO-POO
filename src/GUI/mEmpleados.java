    public void generarFila(Empleado emp){
        String[] cdn = new String[4];
        cdn[0] = emp.getNombres();
        cdn[1] = emp.getApellidos();
        cdn[2] = emp.getDni();
        cdn[3] = emp.getRol(); 
        model.addRow(cdn);
    }

